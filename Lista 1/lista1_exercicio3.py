# 3. Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro
# desta área para o usuário.

base_quadrado = float(input("Digite a base do quadrado: "))
altura_quadrado = float(input("Digite a altura do quadrado: "))
area_quadrado = base_quadrado*altura_quadrado
print(f"A área do quadrado é {area_quadrado} centímetros")
